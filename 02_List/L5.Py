# Insert

l1=[10,20,30,50,60,70]
l1.insert(3,"sai kumar")
print(l1)


# pop method
L2=[10,20,30,40,50,60,70,"sai"]
L2.pop(4)
print(L2)

# index(element) to find the index in a list

L3=[10,20,30,40,50,60,70,"sai"]
print(L3.index(30))

# remove()
# removes the given element from the list

L4=[10,20,30,40,50,60,70,"sai"]
L4.remove(20)
print(L4)

